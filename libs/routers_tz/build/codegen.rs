use serde::Serialize;
use std::env;
use std::fs::File;
use std::io::Write;
use std::path::{Path, PathBuf};

use crate::BoxError;

/// Describes one storage backend: its module name under
/// `routers_tz_types::storage::{module}` and the type name of its
/// deserialised form.
pub struct Backend<'a> {
    pub module: &'a str,
    pub type_name: &'a str,
}

impl Backend<'_> {
    /// Serialise `value` into the backend's data file and emit the
    /// matching `include!`-able accessor module.
    pub fn emit(&self, value: impl Serialize) -> Result<(), BoxError> {
        self.write_data(value)?;
        self.write_codegen()
    }

    pub fn data_path(&self) -> Result<PathBuf, BoxError> {
        let out_dir = env::var("OUT_DIR")?;
        Ok(Path::new(&out_dir).join(format!("{}_timezone_data.bin", self.module)))
    }

    pub fn write_data(&self, value: impl Serialize) -> Result<(), BoxError> {
        let bytes = postcard::to_allocvec(&value)
            .map_err(|e| format!("failed to serialise {}: {e}", self.module))?;
        File::create(self.data_path()?)?.write_all(&bytes)?;
        Ok(())
    }

    pub fn write_codegen(&self) -> Result<(), BoxError> {
        let out_dir = env::var("OUT_DIR")?;
        let path = Path::new(&out_dir).join(format!("{}_timezone_storage.rs", self.module));
        let data_binary = format!("{}_timezone_data.bin", self.module);
        let module = self.module;
        let type_name = self.type_name;

        eprintln!("Writing to {path:?}");

        let mut f = File::create(&path)?;
        writeln!(
            f,
            r#"
// Code generated by @bennjii 2026
use lazy_static::lazy_static;
use routers_tz_types::storage::{module}::*;

const TIMEZONE_DATA: &[u8] = include_bytes!("{data_binary}");

lazy_static! {{
    pub static ref STORAGE: {type_name} = {{
        postcard::from_bytes(TIMEZONE_DATA)
            .expect("Failed to deserialize {module} timezone data")
    }};
}}

pub fn storage() -> &'static {type_name} {{
    &STORAGE
}}
"#
        )?;
        Ok(())
    }
}
